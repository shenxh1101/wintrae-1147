public class TemplateComparison
{
    public string ComparisonId { get; set; } = Guid.NewGuid().ToString();
    public string TemplateId1 { get; set; } = string.Empty;
    public string TemplateId2 { get; set; } = string.Empty;
    
    public DateTime ComparedAt { get; set; }
    
    public List<TemplateDifference> Differences { get; set; } = new();
    public List<TemplateDifference> SignificantDifferences { get; set; } = new();
    
    public decimal TotalDifference { get; set; }
    public decimal PercentageDifference { get; set; }
    
    public string GenerateComparisonReport()
    {
        var report = new List<string>();
        
        report.Add("═══════════════════════════════════════════════════════════");
        report.Add("              口径模板对比报告                           ");
        report.Add("═══════════════════════════════════════════════════════════");
        report.Add($"模板1: {TemplateId1}");
        report.Add($"模板2: {TemplateId2}");
        report.Add($"对比时间: {ComparedAt:yyyy-MM-dd HH:mm:ss}");
        report.Add("");
        
        if (!SignificantDifferences.Any())
        {
            report.Add("【对比结果】");
            report.Add("  ✓ 无显著差异，口径一致");
        }
        else
        {
            report.Add($"【差异汇总】共 {SignificantDifferences.Count} 项显著差异");
            report.Add($"  总金额差异: ¥{TotalDifference:F2}");
            report.Add($"  百分比差异: {PercentageDifference:P2}");
            report.Add("");
            
            report.Add("【差异明细】");
            foreach (var diff in SignificantDifferences)
            {
                report.Add($"  {diff.Category}.{diff.Field}");
                report.Add($"    模板1: {diff.OldValue}");
                report.Add($"    模板2: {diff.NewValue}");
                report.Add($"    说明: {diff.Description}");
            }
        }
        
        report.Add("═══════════════════════════════════════════════════════════");
        
        return string.Join(Environment.NewLine, report);
    }
}

