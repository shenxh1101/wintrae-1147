public class TemplateManagementService
{
    private readonly List<CalculationTemplate> _templates = new();
    
    public CalculationTemplate CreateTemplate(
        string name,
        string category,
        TemplateType type,
        TemplateSnapshot snapshot,
        string createdBy,
        string description = "")
    {
        var template = new CalculationTemplate
        {
            TemplateName = name,
            TemplateDescription = description,
            Category = category,
            Type = type,
            Source = TemplateSource.Manual,
            CreatedAt = DateTime.UtcNow,
            CreatedBy = createdBy,
            Snapshot = snapshot,
            IsActive = true
        };
        
        _templates.Add(template);
        return template;
    }
    
    public CalculationTemplate? GetTemplate(string templateId)
    {
        return _templates.FirstOrDefault(t => t.TemplateId == templateId);
    }
    
    public List<CalculationTemplate> SearchTemplates(TemplateSearchCriteria criteria)
    {
        var query = _templates.AsEnumerable();
        
        if (!string.IsNullOrEmpty(criteria.Name))
            query = query.Where(t => t.TemplateName.Contains(criteria.Name));
        
        if (!string.IsNullOrEmpty(criteria.Category))
            query = query.Where(t => t.Category == criteria.Category);
        
        if (criteria.Type.HasValue)
            query = query.Where(t => t.Type == criteria.Type.Value);
        
        if (criteria.Source.HasValue)
            query = query.Where(t => t.Source == criteria.Source.Value);
        
        if (criteria.CreatedAfter.HasValue)
            query = query.Where(t => t.CreatedAt >= criteria.CreatedAfter.Value);
        
        if (criteria.CreatedBefore.HasValue)
            query = query.Where(t => t.CreatedAt <= criteria.CreatedBefore.Value);
        
        if (!string.IsNullOrEmpty(criteria.CreatedBy))
            query = query.Where(t => t.CreatedBy == criteria.CreatedBy);
        
        if (criteria.MinUsageCount.HasValue)
            query = query.Where(t => t.UsageCount >= criteria.MinUsageCount.Value);
        
        if (criteria.IsActive.HasValue)
            query = query.Where(t => t.IsActive == criteria.IsActive.Value);
        
        return query.OrderByDescending(t => t.LastUsedAt ?? t.CreatedAt).ToList();
    }
    
    public CalculationTemplate UpdateTemplate(string templateId, Action<CalculationTemplate> updateAction)
    {
        var template = GetTemplate(templateId);
        if (template == null)
            throw new ArgumentException("Template not found");
        
        updateAction(template);
        template.Versions.Add(new TemplateVersion
        {
            VersionNumber = template.Versions.Count + 1,
            CreatedAt = DateTime.UtcNow,
            CreatedBy = "System",
            ChangeNote = "Template updated",
            Snapshot = template.Snapshot
        });
        
        return template;
    }
    
    public bool DeleteTemplate(string templateId)
    {
        var template = GetTemplate(templateId);
        if (template == null) return false;
        
        template.IsActive = false;
        return true;
    }
    
    public void RecordUsage(string templateId)
    {
        var template = GetTemplate(templateId);
        if (template != null)
        {
            template.UsageCount++;
            template.LastUsedAt = DateTime.UtcNow;
        }
    }
    
    public List<CalculationTemplate> GetTemplatesByOrder(string orderId)
    {
        return _templates
            .Where(t => t.Snapshot.OrderSnapshot != null && 
                       t.Snapshot.OrderId == orderId)
            .ToList();
    }
    
    public List<CalculationTemplate> GetSimilarTemplates(TemplateSnapshot snapshot, int limit = 10)
    {
        return _templates
            .Where(t => t.IsActive)
            .Select(t => new 
            { 
                Template = t, 
                Score = CalculateSimilarity(t.Snapshot, snapshot) 
            })
            .Where(x => x.Score > 0.5m)
            .OrderByDescending(x => x.Score)
            .Take(limit)
            .Select(x => x.Template)
            .ToList();
    }
    
    private decimal CalculateSimilarity(TemplateSnapshot t1, TemplateSnapshot t2)
    {
        var score = 0m;
        var total = 0m;
        
        if (t1.OrderSnapshot.OriginalAmount == t2.OrderSnapshot.OriginalAmount)
            score += 1m;
        total += 1m;
        
        if (t1.OrderSnapshot.ItemCount == t2.OrderSnapshot.ItemCount)
            score += 1m;
        total += 1m;
        
        if (t1.OrderSnapshot.Member?.Level == t2.OrderSnapshot.Member?.Level)
            score += 1m;
        total += 1m;
        
        var commonCoupons = t1.CouponsSnapshot
            .Select(c => c.CouponId)
            .Intersect(t2.CouponsSnapshot.Select(c => c.CouponId))
            .Count();
        
        var totalCoupons = t1.CouponsSnapshot.Count + t2.CouponsSnapshot.Count;
        if (totalCoupons > 0)
        {
            score += (2m * commonCoupons / totalCoupons);
            total += 1m;
        }
        
        return total > 0 ? score / total : 0;
    }
}

public class UnifiedExplanation
{
    public string ExplanationId { get; set; } = Guid.NewGuid().ToString();
    
    public string OrderId { get; set; } = string.Empty;
    public string TemplateId { get; set; } = string.Empty;
    
    public DateTime GeneratedAt { get; set; }
    public string GeneratedBy { get; set; } = string.Empty;
    
    public AudienceType Audience { get; set; }
    
    public UnifiedExplanationContent Content { get; set; } = new();
    
    public Dictionary<string, object> RawData { get; set; } = new();
    
    public string GenerateForAudience(AudienceType audience)
    {
        return audience switch
        {
            AudienceType.Customer => GenerateCustomerExplanation(),
            AudienceType.CustomerService => GenerateCustomerServiceExplanation(),
            AudienceType.Operations => GenerateOperationsExplanation(),
            AudienceType.Finance => GenerateFinanceExplanation(),
            _ => GenerateDefaultExplanation()
        };
    }
    
    public string GenerateCustomerExplanation()
    {
        var lines = new List<string>();
        lines.Add($"订单号: {OrderId}");
        lines.Add($"应付金额: ¥{Content.FinalAmount:F2}");
        
        if (Content.AppliedDiscounts.Any())
        {
            lines.Add("已享受优惠:");
            foreach (var discount in Content.AppliedDiscounts)
            {
                lines.Add($"  · {discount.Name}: -¥{discount.Amount:F2}");
            }
        }
        
        if (!string.IsNullOrEmpty(Content.CustomerMessage))
            lines.Add(Content.CustomerMessage);
        
        return string.Join(Environment.NewLine, lines);
    }
    
    public string GenerateCustomerServiceExplanation()
    {
        var lines = new List<string>();
        lines.Add($"【客服口径】订单号: {OrderId}");
        lines.Add($"原始金额: ¥{Content.OriginalAmount:F2}");
        lines.Add($"优惠金额: -¥{Content.TotalDiscount:F2}");
        lines.Add($"应付金额: ¥{Content.FinalAmount:F2}");
        
        lines.Add("");
        lines.Add("【优惠明细】");
        foreach (var discount in Content.DiscountDetails)
        {
            lines.Add($"  {discount.Type}: ¥{discount.Amount:F2}");
            lines.Add($"    原因: {discount.Reason}");
        }
        
        if (Content.UnavailableCoupons.Any())
        {
            lines.Add("");
            lines.Add("【不可用券】");
            foreach (var coupon in Content.UnavailableCoupons)
            {
                lines.Add($"  · {coupon.Name}: {coupon.Reason}");
            }
        }
        
        return string.Join(Environment.NewLine, lines);
    }
    
    public string GenerateOperationsExplanation()
    {
        var lines = new List<string>();
        lines.Add($"【运营口径】订单号: {OrderId}");
        lines.Add($"原始金额: ¥{Content.OriginalAmount:F2}");
        lines.Add($"总优惠: -¥{Content.TotalDiscount:F2}");
        lines.Add($"优惠率: {Content.DiscountRate:P2}");
        lines.Add($"应付金额: ¥{Content.FinalAmount:F2}");
        
        lines.Add("");
        lines.Add("【应用规则】");
        foreach (var rule in Content.AppliedRules)
        {
            lines.Add($"  · {rule.RuleName}: 贡献 ¥{rule.Contribution:F2}");
        }
        
        lines.Add("");
        lines.Add("【口径快照ID】: {TemplateId}");
        lines.Add($"【生成时间】: {GeneratedAt:yyyy-MM-dd HH:mm:ss}");
        
        return string.Join(Environment.NewLine, lines);
    }
    
    public string GenerateFinanceExplanation()
    {
        var lines = new List<string>();
        lines.Add($"【财务口径】订单号: {OrderId}");
        lines.Add($"商品总额: ¥{Content.OriginalAmount:F2}");
        lines.Add($"运费: ¥{Content.Freight:F2}");
        lines.Add($"应付总额: ¥{Content.FinalAmount + Content.Freight:F2}");
        lines.Add($"优惠总额: ¥{Content.TotalDiscount:F2}");
        lines.Add($"净应付: ¥{Content.FinalAmount:F2}");
        
        lines.Add("");
        lines.Add("【优惠明细(财务口径)】");
        foreach (var discount in Content.DiscountDetails)
        {
            lines.Add($"  {discount.Type}: ¥{discount.Amount:F2} (税率处理: {discount.TaxTreatment})");
        }
        
        lines.Add("");
        lines.Add($"【口径快照ID】: {TemplateId}");
        lines.Add($"【快照时间】: {GeneratedAt:yyyy-MM-dd HH:mm:ss}");
        
        return string.Join(Environment.NewLine, lines);
    }
    
    private string GenerateDefaultExplanation()
    {
        return GenerateCustomerServiceExplanation();
    }
}

